using namespace std;

class Solution {
public:
    bool isUgly(int num) {
        long n = num;
        while(n % 2 == 0) n /= 2;
        while(n % 3 == 0) n /= 3;
        while(n % 5 == 0) n /= 5;
        std::cout<<n<<std::endl;
        return std::abs(n) == 1;
    }
};

int main() {
    Solution s;
    s.isUgly(-2147483648);
}
