using namespace std;

class Solution {
public:
    int findPeakElement(const vector<int> &num)
    {
        int low = 0;
        int high = num.size()-1;

        while(low < high)
        {
            int mid1 = (low+high)/2;
            int mid2 = mid1+1;
            if(num[mid1] < num[mid2])
                low = mid2;
            else
                high = mid1;
        }
        return low;
    }
};

int main()
{
    Solution s;
    vector<int> a{1, 6, 2, 3, 4, 5};
    std::cout<<s.findPeakElement(a)<<std::endl;
}
